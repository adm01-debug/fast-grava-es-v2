export const getTechniqueById = (id: TechniqueId): Technique | undefined => {
  return techniques.find(t => t.id === id);
};

export const getMachinesByTechnique = (techniqueId: TechniqueId): Machine[] => {
  return machines.filter(m => m.techniqueId === techniqueId);
};

export const getOperatorsByMachine = (machineId: string): Operator[] => {
  return operators.filter(op => op.machineIds.includes(machineId));
};

export const getMachineById = (machineId: string): Machine | undefined => {
  return machines.find(m => m.id === machineId);
};

export const getOperatorById = (operatorId: string): Operator | undefined => {
  return operators.find(o => o.id === operatorId);
};
